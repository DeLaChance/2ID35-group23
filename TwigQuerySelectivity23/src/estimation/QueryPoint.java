package estimation;

/**
 *
 * @author rik
 */
class QueryPoint {
    // min x, max y
    private int x,y;
    
    public QueryPoint(int x, int y)
    {
        this.x = x;
        this.y = y;
    }
    
    public int getX()
    {
        return x;
    }
    
    public int getY()
    {
        return y;
    }
}
